package be.intecbrussel.Les5;

public class ForLoopExample {
    public static void main(String[] args) {
        for (int index = 0; index < 10; index++){
            System.out.println("The index is:" +index);
        }
        for (int index = 0; index<10; index++){
            System.out.println("The index is:"+index);
        }
        for (int index = 10; index>=0; index--){
            System.out.println("The index is:" + index);
        }
    }

}
